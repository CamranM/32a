# program converts the farenheit variable given in farenheit degrees into
# celcius degrees using the farenheit to celcius formula. Then, it prints out
# the celcius temperature.


# variable holds the degrees farenheit value that will be converted to celsius
fahrenheit = 32

#variable holds the formula for converting celsius into fahrenheit
celsius = (fahrenheit - 32.0) * (5 / 9)

# output result that prints the converted celsius degrees 
print(fahrenheit, "Fahrenheit is", celsius, "Celsius.")
