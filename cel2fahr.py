# converts celsius to fahrenheit with the celsius to fahrenheit formula
# prints converted fahrenheit degrees

# variable that holds cesius value that will be converted 
celsius = 32

# formula that converts given degrees celsius into fahrenheit
cel_to_fahren = celsius * (9/5) + 32

# output result that prints the converted fahrenheit degrees 
print(celsius, "Celsius is", cel_to_fahren, "Fahrenheit.")
